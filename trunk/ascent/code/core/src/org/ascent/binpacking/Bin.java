package org.ascent.binpacking;

public class Bin {

	private int[] size_;
	private String name_;

	public int[] getSize() {
		return size_;
	}

	public void setSize(int[] size) {
		size_ = size;
	}

	public String getName() {
		return name_;
	}

	public void setName(String name) {
		name_ = name;
	}

}
